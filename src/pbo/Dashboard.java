    private DefaultTableModel tableModel;
   
    private void getTabelHistory()
    {
        tableModel = new DefaultTableModel();
        history.setModel(tableModel);
        tableModel.addColumn("Id Pembayaran");
        tableModel.addColumn("Jenis Jasa");
        tableModel.addColumn("Total Harga");
        tabelHistory();
    }
    
    private void tabelHistory()
    {
        try {
            Statement smt = conn.createStatement();
            String query = "SELECT * FROM pembayaran";
            ResultSet result =  smt.executeQuery(query);
            tableModel.setRowCount(0);
            
            while(result.next()){
                String col1Value = result.getString("id");
                String col2Value = result.getString("jasa");
                String col3Value = result.getString("total_harga");
                
                Object[] rowData = {col1Value,col2Value,col3Value};
                tableModel.addRow(rowData);
            }
            result.close();
            smt.close();
            conn.clearWarnings();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private boolean createPembayaran(String jasa,double hargaBahan,double hargaJasa,double totalBarang)
    {
        double totalHarga = 0;
        try {
           
            totalHarga = ( hargaBahan + hargaJasa ) * totalBarang;
            String query = "INSERT INTO pembayaran (id,jasa,total_harga) VALUES (null,?,?)";
            PreparedStatement smt = conn.prepareStatement(query);
            smt.setString(1, jasa);
            smt.setDouble(2, totalHarga);
            
            int rowsAffected = smt.executeUpdate();
            if (rowsAffected > 0) {
                System.out.println("Data berhasil dimasukkan.");
            } else {
                System.out.println("Gagal memasukkan data.");
            }
            
            return true;
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Gagal membuat Pembayaran");
        }
        
       return false;
    }
    
    
    

    private void cetakHistoryDesainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakHistoryDesainActionPerformed
        String jasa = this.jasaDesain.getSelectedItem().toString();    
        String hargaBahan = hargaBahanDesain.getText();
        String hargaJasa = hargaJasaDesain.getText();
        String totalBarang = totalBarangDesain.getText();
    
    try {
        double hargaBahanValue = Double.parseDouble(hargaBahan);
        double hargaJasaValue = Double.parseDouble(hargaJasa);
        double totalBarangValue = Double.parseDouble(totalBarang);
        
        if(createPembayaran(jasa, hargaBahanValue, hargaJasaValue, totalBarangValue)) {
            JOptionPane.showMessageDialog(this, "Berhasil membuat pembayaran");  
            getTabelHistory();
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Input harga barang, harga jasa, atau total barang tidak valid.");
    }
      
    }//GEN-LAST:event_cetakHistoryDesainActionPerformed

